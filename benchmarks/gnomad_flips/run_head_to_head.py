#!/usr/bin/env python3
"""
hapli vs bcftools/csq compound-variant head-to-head.

For each curated compound-variant case (currently the paper-cases bundles
generated by `scripts/generate_paper_cases.py`):

  1. Run `hapli analyze --with-esm` on the case bundle.
  2. Read back the resulting JSON (the schema-v2 evidence block already
     includes bcftools/csq output under `evidence.consequence`).
  3. Project both views to a comparable per-haplotype call:
       - csq_view:    set of consequence labels per haplotype (LoF if
                      contains stop_gained/frameshift/splice_*; missense
                      otherwise; empty if no consequence rows).
       - hapli_view:  per-haplotype score from the diploid aggregator,
                      compound_het_lof flag, residual flagged status.
  4. Emit a per-case row showing where the two views disagree.

This is the gnomAD-flip protocol (Danecek 2017) applied to a small,
curated set of cases that exercise specifically the disagreement axes
the paper hinges on:

  - Compound-het LoF aggregation: csq emits two LoF records, hapli
    emits one diploid `compound_het_lof=True`.
  - Frameshift+rescue (case 05): csq tags the pair `inframe_altering`
    correctly, hapli's protein-diff additionally flags `frameshift_region.restored=True`.
  - Compound missense in a pocket (case 06): csq emits two missense
    records, hapli's ESM2 epistasis residual flags the joint effect
    (`flagged=True` if |residual| > threshold).
  - Symbolic SV deletion (case 08): csq emits nothing (gene gone),
    hapli's presence call says `deleted` and diploid score=0.

The full Danecek 2017 reproduction on 1000G Phase 3 follows the same
protocol at scale; that needs ~80 GB of input data and is documented
in `benchmarks/gnomad_flips/README.md` as the scaled-up version of
this script.
"""

from __future__ import annotations

import argparse
import json
import shutil
import subprocess
import sys
from pathlib import Path


# Map case dir name → (gene name in the case GFF, sample name in the case VCF)
# All 12 curated cases are expected to round-trip through csq + hapli; the
# head-to-head report shows where the two views agree and where hapli has
# a signal csq cannot express.
CASES = {
    "01_synonymous_pair":           ("G1", "S1"),
    "02_single_benign_missense":    ("G1", "S1"),
    "03_single_stop_gain":          ("G1", "S1"),
    "04_compound_het_lof":          ("G1", "S1"),
    "05_frameshift_rescue":         ("G1", "S1"),
    "06_compound_missense_pocket":  ("G1", "S1"),
    "07_splice_donor_cryptic":      ("G1", "S1"),
    "08_symbolic_del_whole_gene":   ("G1", "S1"),
    "09_upstream_del_shift":        ("G1", "S1"),
    "10_inversion_body":            ("G1", "S1"),
    "11_tandem_dup_small_gene":     ("G1", "S1"),
    "12_inframe_domain_indel":      ("G1", "S1"),
}

LOF_TAGS = {
    "stop_gained", "stop_lost", "start_lost", "frameshift",
    "splice_acceptor", "splice_donor",
}


def _csq_view(consequences: list[dict]) -> dict[int, dict]:
    """Project csq's consequence list to per-haplotype: which tags landed,
    whether any of them are LoF-class, count of compound rows."""
    by_hap: dict[int, dict] = {}
    for rec in consequences:
        h = rec["haplotype"]
        s = by_hap.setdefault(h, {"consequences": set(), "n_records": 0, "compound_ids": set(), "transcripts": set()})
        s["consequences"].add(rec["consequence"])
        s["n_records"] += 1
        if rec.get("compound_id"):
            s["compound_ids"].add(rec["compound_id"])
        if rec.get("transcript"):
            s["transcripts"].add(rec["transcript"])
    out: dict[int, dict] = {}
    for h, s in by_hap.items():
        any_lof = any(c in LOF_TAGS for c in s["consequences"])
        out[h] = {
            "consequences": sorted(s["consequences"]),
            "lof_class": any_lof,
            "n_records": s["n_records"],
            "compound_ids": sorted(s["compound_ids"]),
            "transcripts": sorted(s["transcripts"]),
        }
    return out


def _hapli_view(data: dict) -> dict:
    """Pull the haplotype-level call from the diploid aggregator + epistasis flag."""
    ev = data.get("evidence", {})
    dip = ev.get("diploid", {})
    presence = ev.get("presence", {})
    epi_flagged = any(e.get("flagged") for e in ev.get("epistasis", []))
    return {
        "presence_hap1": presence.get("hap1", {}).get("status"),
        "presence_hap2": presence.get("hap2", {}).get("status"),
        "hap1_score": dip.get("hap1_score"),
        "hap2_score": dip.get("hap2_score"),
        "compound_het_lof": dip.get("compound_het_lof"),
        "epistasis_flagged": epi_flagged,
    }


def _run_hapli(case_dir: Path, out_dir: Path, gene: str, sample: str, with_esm: bool) -> dict:
    cmd = [
        "uv", "run", "main.py", "analyze",
        "--gene", gene, "--sample", sample,
        "--vcf",       str(case_dir / "phased.vcf.gz"),
        "--reference", str(case_dir / "reference.fa"),
        "--gff",       str(case_dir / "annotation.gff3"),
        "--output-dir", str(out_dir),
    ]
    if with_esm:
        cmd.append("--with-esm")
    repo = Path(__file__).parent.parent.parent
    res = subprocess.run(cmd, cwd=repo, capture_output=True, text=True, timeout=600)
    if res.returncode != 0:
        raise RuntimeError(
            f"hapli analyze failed on {case_dir.name}\n"
            f"stdout:\n{res.stdout}\n"
            f"stderr:\n{res.stderr}"
        )
    json_path = out_dir / f"{sample}_{gene}_analysis.json"
    return json.loads(json_path.read_text())


def main(argv: list[str] | None = None) -> int:
    ap = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    ap.add_argument("--cases-dir", type=Path, default=Path("data/paper_cases"))
    ap.add_argument("--out", type=Path, default=Path("benchmarks/gnomad_flips/results/head_to_head.json"))
    ap.add_argument("--with-esm", action="store_true",
                    help="also compute hapli's ESM epistasis residual (the case-06 column).")
    ap.add_argument("--keep-results", action="store_true",
                    help="don't delete per-case hapli output dirs after parsing")
    args = ap.parse_args(argv)

    args.out.parent.mkdir(parents=True, exist_ok=True)
    rows: list[dict] = []

    for case_name, (gene, sample) in CASES.items():
        case_dir = args.cases_dir / case_name
        if not case_dir.exists():
            print(f"[skip] {case_name} bundle missing — generate it first with "
                  f"scripts/generate_paper_cases.py", file=sys.stderr)
            continue
        out_dir = args.out.parent / case_name
        if out_dir.exists() and not args.keep_results:
            shutil.rmtree(out_dir)
        try:
            data = _run_hapli(case_dir, out_dir, gene, sample, args.with_esm)
        except RuntimeError as exc:
            print(f"[fail] {case_name}: {exc}", file=sys.stderr)
            rows.append({"case": case_name, "error": str(exc)})
            continue

        csq = _csq_view(data.get("evidence", {}).get("consequence", []))
        hapli = _hapli_view(data)
        # Disagreements that matter for the paper:
        disagreement = []
        if hapli.get("compound_het_lof"):
            # csq does not emit a "compound het LoF" call by design — this is
            # always a hapli-only signal.
            disagreement.append("compound_het_lof_only_in_hapli")
        if args.with_esm and hapli.get("epistasis_flagged"):
            # csq has no epistasis signal — also a hapli-only contribution.
            disagreement.append("epistasis_only_in_hapli")
        # SV-deletion: csq sees nothing (gene removed from haplotype) but
        # hapli's presence call says "deleted".
        for h_key in ("presence_hap1", "presence_hap2"):
            if hapli.get(h_key) == "deleted":
                disagreement.append(f"{h_key}=deleted_only_in_hapli")
        rows.append({
            "case": case_name,
            "gene": gene, "sample": sample,
            "csq": csq,
            "hapli": hapli,
            "hapli_only_signals": disagreement,
        })
        if not args.keep_results:
            shutil.rmtree(out_dir, ignore_errors=True)

    # Tally signals
    n_with_compound_het_lof = sum(
        1 for r in rows if r.get("hapli", {}).get("compound_het_lof") is True
    )
    n_with_epistasis = sum(
        1 for r in rows if r.get("hapli", {}).get("epistasis_flagged") is True
    )
    n_csq_only_lof_records = sum(
        sum(1 for h in r.get("csq", {}).values() if h.get("lof_class"))
        for r in rows
    )

    summary = {
        "n_cases": len(rows),
        "tally": {
            "hapli_compound_het_lof_calls": n_with_compound_het_lof,
            "hapli_epistasis_flagged_calls": n_with_epistasis,
            "csq_lof_haplotype_records":     n_csq_only_lof_records,
        },
        "rows": rows,
    }
    args.out.write_text(json.dumps(summary, indent=2))

    # Pretty-print a one-line-per-case ASCII table
    print(f"{'case':<32} {'csq.hap1':<32} {'csq.hap2':<32} {'hapli.dip':<25} {'hapli-only signals'}")
    print("-" * 140)
    for r in rows:
        if "error" in r:
            print(f"{r['case']:<32} ERROR: {r['error'][:80]}")
            continue
        c = r["csq"]
        h = r["hapli"]
        c1 = ",".join(c.get(1, {}).get("consequences", [])) or "-"
        c2 = ",".join(c.get(2, {}).get("consequences", [])) or "-"
        h1 = h.get("hap1_score"); h2 = h.get("hap2_score")
        chl = "✓" if h.get("compound_het_lof") else "."
        epi = "✓" if h.get("epistasis_flagged") else "."
        signals = ",".join(r["hapli_only_signals"]) or "-"
        print(f"{r['case']:<32} {c1[:30]:<32} {c2[:30]:<32} "
              f"h1={h1 if h1 is None else f'{h1:.2f}'} h2={h2 if h2 is None else f'{h2:.2f}'} "
              f"chl={chl} epi={epi}    {signals}")
    print("-" * 140)
    print(f"Summary: {summary['tally']}")
    print(f"wrote {args.out}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
